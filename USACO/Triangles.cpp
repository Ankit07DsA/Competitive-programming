void solve()
{
    int n, ans = 0;
    cin >> n;

    vector<int> x(N), y(N);
    for (int i = 0; i < n; i++)
        cin >> x[i] >> y[i];

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            for (int k = 0; k < n; k++)
            {
                // the first x coordinate must match to the second x cooordinate,similarly with the y coordinate
                // Check to see if both points are parallel to the X and Y axis
                if (y[i] == y[j] && x[j] == x[k])
                {
                    int x1 = x[i], x2 = x[j], y1 = y[i], y2 = y[k];
                    int area = abs((x2 - x1) * abs(y2 - y1));

                    ans = max(ans, area);
                }
            }
        }
    }
    cout << ans << endl;
}
